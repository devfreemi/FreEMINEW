package com.freemi.entity.general;

public class Folios {

	private String ActivatedAmount;
	private String DefaultFlag;
	private String EmailID;
	private String Folio_No;
	private String InvestorName;
	private String LiquidFlag;
	private String MAXDATE;
	private String MobileNumber;
	private String PANNo;
	public String getActivatedAmount() {
		return ActivatedAmount;
	}
	public void setActivatedAmount(String activatedAmount) {
		ActivatedAmount = activatedAmount;
	}
	public String getDefaultFlag() {
		return DefaultFlag;
	}
	public void setDefaultFlag(String defaultFlag) {
		DefaultFlag = defaultFlag;
	}
	public String getEmailID() {
		return EmailID;
	}
	public void setEmailID(String emailID) {
		EmailID = emailID;
	}
	public String getFolio_No() {
		return Folio_No;
	}
	public void setFolio_No(String folio_No) {
		Folio_No = folio_No;
	}
	public String getInvestorName() {
		return InvestorName;
	}
	public void setInvestorName(String investorName) {
		InvestorName = investorName;
	}
	public String getLiquidFlag() {
		return LiquidFlag;
	}
	public void setLiquidFlag(String liquidFlag) {
		LiquidFlag = liquidFlag;
	}
	public String getMAXDATE() {
		return MAXDATE;
	}
	public void setMAXDATE(String mAXDATE) {
		MAXDATE = mAXDATE;
	}
	public String getMobileNumber() {
		return MobileNumber;
	}
	public void setMobileNumber(String mobileNumber) {
		MobileNumber = mobileNumber;
	}
	public String getPANNo() {
		return PANNo;
	}
	public void setPANNo(String pANNo) {
		PANNo = pANNo;
	}
	
	
}
