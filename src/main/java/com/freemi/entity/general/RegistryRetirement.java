package com.freemi.entity.general;

public class RegistryRetirement {
	
	private int tenure;
	private int currentExpense;
	private int inflation;
	private int currentEquityInvestment;
	private int currentFDDebtInvestment;
	private int expectedReturn;
	private int retirementCorpusRequired;
	public int getTenure() {
		return tenure;
	}
	public void setTenure(int tenure) {
		this.tenure = tenure;
	}
	public int getCurrentExpense() {
		return currentExpense;
	}
	public void setCurrentExpense(int currentExpense) {
		this.currentExpense = currentExpense;
	}
	public int getInflation() {
		return inflation;
	}
	public void setInflation(int inflation) {
		this.inflation = inflation;
	}
	public int getCurrentEquityInvestment() {
		return currentEquityInvestment;
	}
	public void setCurrentEquityInvestment(int currentEquityInvestment) {
		this.currentEquityInvestment = currentEquityInvestment;
	}
	public int getCurrentFDDebtInvestment() {
		return currentFDDebtInvestment;
	}
	public void setCurrentFDDebtInvestment(int currentFDDebtInvestment) {
		this.currentFDDebtInvestment = currentFDDebtInvestment;
	}
	public int getExpectedReturn() {
		return expectedReturn;
	}
	public void setExpectedReturn(int expectedReturn) {
		this.expectedReturn = expectedReturn;
	}
	public int getRetirementCorpusRequired() {
		return retirementCorpusRequired;
	}
	public void setRetirementCorpusRequired(int retirementCorpusRequired) {
		this.retirementCorpusRequired = retirementCorpusRequired;
	}
	
	
}
